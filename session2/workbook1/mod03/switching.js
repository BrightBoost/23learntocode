let grade = "B";

switch (grade) {
    case "F":
        console.log("Not good...");
        break;
    case "D":
        console.log("nope");
        break;
    case "C":
        console.log("C's get degrees");
        break;
    case "B":
        console.log("Studied a little too much!");
        break;
    case "A+":
        console.log("woow!");
    case "A":
        console.log("amazing");
        break;
    default:
        console.log("What grade?");
}